package frc.robot.commands.swervedrive.auto;

import edu.wpi.first.math.geometry.Translation2d;
import edu.wpi.first.math.kinematics.ChassisSpeeds;
import edu.wpi.first.wpilibj2.command.InstantCommand;
import edu.wpi.first.wpilibj2.command.SequentialCommandGroup;
import edu.wpi.first.wpilibj2.command.WaitCommand;
import frc.robot.subsystems.swervedrive.SwerveSubsystem;

// NOTE:  Consider using this command inline, rather than writing a subclass.  For more
// information, see:
// https://docs.wpilib.org/en/stable/docs/software/commandbased/convenience-features.html
public class DriveForwardAuto extends SequentialCommandGroup {
  /** Creates a new DriveForwardAuto. */
  public DriveForwardAuto(SwerveSubsystem drivebase) {
    // Add your commands in the addCommands() call, e.g.
    // addCommands(new FooCommand(), new BarCommand());
    addCommands(
      new InstantCommand(() -> drivebase.drive(new Translation2d(0.5 * drivebase.maximumSpeed, 0), 0, true)),
      new WaitCommand(1),
      new InstantCommand(() -> drivebase.drive(new Translation2d(0, 0), 0, true))
    );
  }
}
